// reActActionBlockRegex extracts individual <action>...</action> blocks from
// within an <actions> container. Uses (?s) for multiline matching.
var reActActionBlockRegex = regexp.MustCompile(`(?s)<action>(.*?)</action>`)

// NBReActPlanner3 is a hybrid ReAct planner that supports parallel tool actions.
// It extends the ReAct loop (thought → action → observation) with an <actions>
// (plural) XML element that allows the LLM to declare multiple independent
// tool calls in a single step.
type NBReActPlanner3 struct {
	ctx                   *security.RequestContext
	llm                   llms.Model
	prompt                prompts.FormatPrompter
	request               NBAgentRequest
	nbAgent               NBAgent
	summaryToolName       string
	retryConfig           RetryConfig
	refinementAttempts    int
	maxRefinementAttempts int
	refinementData        []struct {
		feedback string
		answer   string
	}
	postRefinementToolIndex int
	tools                   []toolcore.NBTool
	enableCritique          bool
	Notebook                string

	// Notebook telemetry — for visibility into whether the agent is
	// actually exercising the notebook discipline the prompt asks for.
	// These are NOT part of planner correctness; they exist so operators
	// can grep logs / replay sessions and validate notebook usage.
	notebookUpdateCount      int // total <update_notebook> emissions seen
	notebookLastUpdateTurn   int // turn index (0-based) of last update; -1 if never
	notebookFirstUpdateTurn  int // turn index of first update; -1 if never
	notebookStaleWarningsLog int // count of stale-notebook warnings emitted

	// notebookAgentID is the llm_conversation_agent row id used to
	// persist the notebook to the DB (mirrors how rewoo stores its
	// generated plan). Created lazily on the first update; subsequent
	// updates patch the same row so the UI sees a single notebook
	// entry that evolves over the course of the investigation.
	notebookAgentID string

	compressionTracker *CompressionTracker
	// llmSummarizationDisabled is set to true after the first LLM summarization
	// failure. Once set, prewarmSummaries skips LLM calls entirely and lets the
	// scratchpad builder fall back to byte truncation. This avoids wasting time
	// on retries + fallback model attempts when the LLM is unavailable.
	llmSummarizationDisabled bool
	// cachedOlderPortionLen and cachedSummaryPrefix avoid redundant LLM
	// summarization calls. When the older portion length hasn't changed, the
	// same summary is reused. This helps both within-iteration (multiple
	// buildScratchpad calls) and across iterations where the split point is stable.
	cachedOlderPortionLen int
	cachedSummaryPrefix   string

	// stepCount is a monotonically increasing counter used to assign
	// human-readable sequential display IDs (E1, E2, E3 ...) to every
	// tool action generated by this planner.  Each call to
	// processToolAction or parseParallelActions increments the counter
	// before assigning DisplayID so the IDs are stable across retries
	// within a single Plan() call.
	stepCount int
}

// notebookDummyAgent is the agent name used when persisting the
// react_3 notebook to llm_conversation_agent. It mirrors how rewoo
// uses plannerDummyTool ("planner") for its generated plan record.
// This row is written via the DAO only — it never enters the
// action queue or scratchpad, so there is no risk of the executor
// trying to invoke it as a real tool.
const notebookDummyAgent = "notebook"

// isNotebookToolName returns true when toolName is a hallucinated variant
// of the notebook update tag. LLMs sometimes emit the notebook content as
// a tool call instead of the inline <update_notebook> XML tag. This
// function covers the observed variant ("update_notebook") plus plausible
// aliases that an LLM might generate from the prompt instructions.
func isNotebookToolName(toolName string) bool {
	lower := strings.ToLower(strings.TrimSpace(toolName))
	switch lower {
	case "update_notebook",
		"notebook",
		"create_notebook",
		"write_notebook",
		"save_notebook",
		"notebook_update",
		"edit_notebook":
		return true
	}
	return false
}

// saveCritique persists the results of a critique cycle to the database.
func (o *NBReActPlanner3) saveCritique(critiqueType, input, critiquedContent, feedback, decision string) error {
	conversationDAO := GetConversationDao()
	var feedbackPtr *string
	if feedback != "" {
		feedbackPtr = &feedback
	}
	return conversationDAO.SaveCritique(&ConversationCritique{
		ConversationID:   o.request.ConversationId,
		MessageID:        o.request.MessageId,
		AccountID:        o.request.AccountId,
		AgentName:        o.nbAgent.GetName(),
		CritiqueType:     critiqueType,
		Input:            input,
		CritiquedContent: critiquedContent,
		Feedback:         feedbackPtr,
		Decision:         decision,
	})
}

// notebookSectionEnabled returns whether the notebook discipline is in
// effect for this planner instance. Mirrors ResolveAgentNotebookEnabled
// so we can early-return from notebook-handling paths (system_nudge
// injection, hallucination capture) when the agent opted out via
// NBAgentNotebookSectionProvider. When nbAgent is nil (test struct
// literals) the default is true — matching pre-opt-out behavior so
// existing tests aren't disturbed.
func (o *NBReActPlanner3) notebookSectionEnabled() bool {
	if o.nbAgent == nil {
		return true
	}
	return ResolveAgentNotebookEnabled(o.nbAgent)
}

// notebookStaleTurnThreshold is the number of turns without a notebook
// update before the planner considers the notebook stale and injects a
// system nudge / logs a warning. Centralised here so the threshold is
// consistent across buildScratchpad and processNotebookUpdate.
const notebookStaleTurnThreshold = 2

// isTopLevelAgent returns true when this planner instance is the
// top-level orchestrator (not a sub-agent like postgres or gcp).
// Sub-agents have a ParentAgentId pointing to the orchestrator;
// top-level agents either have it empty or self-referencing.
func (o *NBReActPlanner3) isTopLevelAgent() bool {
	return o.request.ParentAgentId == "" || o.request.ParentAgentId == o.request.AgentId
}

// notebookStaleInfo returns whether the notebook is stale and how many
// turns have elapsed since the last update (-1 if never updated).
func (o *NBReActPlanner3) notebookStaleInfo(turnIdx int) (stale bool, turnsSinceUpdate int) {
	turnsSinceUpdate = -1
	if o.notebookLastUpdateTurn >= 0 {
		turnsSinceUpdate = turnIdx - o.notebookLastUpdateTurn
	}
	stale = (o.notebookUpdateCount > 0 && turnsSinceUpdate >= notebookStaleTurnThreshold) ||
		(o.notebookUpdateCount == 0 && turnIdx >= notebookStaleTurnThreshold)
	return
}

// stripNestedXMLTags removes common nested XML tags that leak into the thought
// string when the LLM wraps everything inside <thought_action>.
func stripNestedXMLTags(thought string) string {
	for _, tag := range []string{"actions", "action", "tool_name", "tool_input"} {
		thought = strings.ReplaceAll(thought, "<"+tag+">", "")
		thought = strings.ReplaceAll(thought, "</"+tag+">", "")
	}
	return strings.TrimSpace(thought)
}

