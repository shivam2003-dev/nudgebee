// convertToOutputLogs normalizes Dynatrace Grail log records to NudgeBee's OutputLog format.
//
// Mapping:
//
//	record["timestamp"]  → OutputLog.Timestamp
//	record["content"]    → OutputLog.Message
//	record["status"]     → OutputLog.Severity (fall back to "loglevel")
//	all record fields    → OutputLog.Labels
func (s *DynatraceLogSource) convertToOutputLogs(records []map[string]any) []OutputLog {
	logs := make([]OutputLog, 0, len(records))
	for _, record := range records {
		log := OutputLog{
			Timestamp: grailStr(record, "timestamp"),
			Message:   grailStr(record, "content"),
			Labels:    make(map[string]any, len(record)),
		}

		// Severity: prefer "status", fall back to "loglevel"
		if status := grailStr(record, "status"); status != "" {
			log.Severity = status
		} else if loglevel := grailStr(record, "loglevel"); loglevel != "" {
			log.Severity = loglevel
		}

		// Flatten all record fields into labels
		for k, v := range record {
			log.Labels[k] = v
		}

		logs = append(logs, log)
	}
	return logs
}

// getTimeRange converts epoch ms timestamps to ISO 8601 strings required by the Dynatrace Grail API.
func (s *DynatraceLogSource) getTimeRange(startTime, endTime int64) (string, string) {
	return dynatraceTimeRange(startTime, endTime)
}

// dynatraceTimeRange converts epoch timestamps to ISO 8601 strings for Dynatrace Grail API calls.
// Accepts epoch-seconds (<1e12), epoch-ms (1e12–1e15), or epoch-µs (>1e15) and normalizes to ms.
// Defaults to the last hour when a value is zero.
func dynatraceTimeRange(startTime, endTime int64) (string, string) {
	if startTime == 0 {
		startTime = time.Now().Add(-1 * time.Hour).UnixMilli()
	}
	if endTime == 0 {
		endTime = time.Now().UnixMilli()
	}
	// Normalize to milliseconds.
	if startTime < 1e12 {
		startTime *= 1000 // seconds → ms
	} else if startTime > 1e15 {
		startTime /= 1000 // microseconds → ms
	}
	if endTime < 1e12 {
		endTime *= 1000 // seconds → ms
	} else if endTime > 1e15 {
		endTime /= 1000 // microseconds → ms
	}
	return time.UnixMilli(startTime).UTC().Format(time.RFC3339),
		time.UnixMilli(endTime).UTC().Format(time.RFC3339)
}

