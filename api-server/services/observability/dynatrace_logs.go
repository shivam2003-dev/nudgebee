// DynatraceLogGroupSource queries Dynatrace Grail for aggregated error/critical
// log patterns grouped by message, namespace, workload, and container.
type DynatraceLogGroupSource struct{}

// QueryLogGroup fetches log group aggregations from Dynatrace Grail using DQL summarize.
func (s *DynatraceLogGroupSource) QueryLogGroup(ctx *security.RequestContext, req FetchLogGroupRequest) (LogGroupOutput, error) {
	apiToken, baseURL, err := integrations.GetDynatraceConfigs(ctx, req.AccountId)
	if err != nil {
		ctx.GetLogger().Error("DynatraceLogGroupSource.QueryLogGroup: failed to get configs", "error", err)
		return LogGroupOutput{}, fmt.Errorf("failed to get Dynatrace configs: %w", err)
	}

	from, to := dynatraceTimeRange(req.StartTime, req.EndTime)
	dql := s.buildLogGroupDQL(req, from, to)
	ctx.GetLogger().Info("DynatraceLogGroupSource.QueryLogGroup", "dql", dql)

	result, err := executeDQLQuery(baseURL, apiToken, dql)
	if err != nil {
		ctx.GetLogger().Error("DynatraceLogGroupSource.QueryLogGroup: DQL query failed", "error", err)
		if errors.Is(err, integrations.ErrDQLQueryTimeout) {
			return LogGroupOutput{}, fmt.Errorf(
				"log group query timed out — the selected time range contains too many logs. " +
					"Please apply more filters: select a specific Namespace or Workload to narrow the scope",
			)
		}
		return LogGroupOutput{}, fmt.Errorf("dynatrace log group query failed: %w", err)
	}

	// Use the end of the query window as the single timestamp for all groups.
	// Return epoch-seconds to match all other log group providers (frontend multiplies by 1000).
	var endTimeSec int64
	if req.EndTime <= 0 {
		endTimeSec = time.Now().Unix()
	} else if req.EndTime >= 1e12 {
		// EndTime is in milliseconds — convert to seconds.
		endTimeSec = req.EndTime / 1000
	} else {
		endTimeSec = req.EndTime
	}

	groups := make([]LogGroup, 0, len(result.Records))
	for _, record := range result.Records {
		r, ok := s.convertToLogGroup(record, endTimeSec)
		if ok {
			groups = append(groups, r)
		}
	}

	return LogGroupOutput{Groups: groups}, nil
}

// buildLogGroupDQL constructs the DQL aggregation query for error/critical log groups.
// Optional filters for selectedNamespace and selectedWorkload are appended when present.
func (s *DynatraceLogGroupSource) buildLogGroupDQL(req FetchLogGroupRequest, from, to string) string {
	var sb strings.Builder

	fmt.Fprintf(&sb, `fetch logs, from: "%s", to: "%s"`, from, to)

	// Severity filter: match on status OR loglevel; fall back to message-content patterns
	// when both fields are absent (handles non-DT-normalised log sources).
	sb.WriteString("\n| filter (")
	sb.WriteString("in(`status`, \"ERROR\", \"CRITICAL\", \"error\", \"critical\", \"Error\", \"Critical\", \"fatal\", \"FATAL\")")
	sb.WriteString(" OR in(`loglevel`, \"error\", \"ERROR\", \"critical\", \"CRITICAL\", \"fatal\", \"FATAL\")")
	sb.WriteString(" OR ((isNull(`status`) OR `status` == \"\") AND (isNull(`loglevel`) OR `loglevel` == \"\")")
	sb.WriteString(" AND (matchesPhrase(content, \"ERROR\") OR matchesPhrase(content, \"FATAL\") OR matchesPhrase(content, \"CRITICAL\"))))")

	// Always exclude known system/infrastructure containers.
	sb.WriteString("\n| filter not(in(`k8s.container.name`, \"prometheus\", \"grafana\", \"nudgebee-agent\"))")

	// Optional namespace filter.
	if ns := common.GetString(req.Request, "selectedNamespace"); ns != "" {
		fmt.Fprintf(&sb, "\n| filter `k8s.namespace.name` == \"%s\"", escapeDQLValue(ns))
	}

	// Optional workload filter: exact match on workload name when present; fall back to
	// pod name substring only when k8s.workload.name is absent — avoids false positives
	// where a pod name substring matches but the workload label points to a different workload.
	if wl := common.GetString(req.Request, "selectedWorkload"); wl != "" {
		escaped := escapeDQLValue(wl)
		fmt.Fprintf(&sb,
			"\n| filter `k8s.workload.name` == \"%s\" OR (isNull(`k8s.workload.name`) AND matchesPhrase(`k8s.pod.name`, \"%s\"))",
			escaped, escaped,
		)
	}

	// Aggregate: one row per unique (message, namespace, workload, container) combination.
	// Using k8s.workload.name (not pod name) ensures all replicas of a workload are merged
	// natively by DQL without any post-processing deduplication.
	sb.WriteString("\n| summarize value = count(), by: {content, `k8s.namespace.name`, `k8s.workload.name`, `k8s.container.name`}")
	sb.WriteString("\n| sort value desc")
	sb.WriteString("\n| limit 100")

	return sb.String()
}

// convertToLogGroup converts a single DQL summarize record into a LogGroup.
// Returns (LogGroup{}, false) for records that are missing required fields.
func (s *DynatraceLogGroupSource) convertToLogGroup(record map[string]any, endTimeSec int64) (LogGroup, bool) {
	// count is a float64 from DQL summarize — must NOT use grailStr.
	// Some DQL responses may return the value as a string; handle both.
	var count float64
	switch v := record["value"].(type) {
	case float64:
		count = v
	case string:
		parsed, err := strconv.ParseFloat(v, 64)
		if err != nil {
			return LogGroup{}, false
		}
		count = parsed
	default:
		return LogGroup{}, false
	}

	// Skip records without a log message — they produce useless empty log groups.
	content := grailStr(record, "content")
	if content == "" {
		return LogGroup{}, false
	}

	// Unicode-safe truncation for display sample (keeps hashes stable by using full content).
	sample := content
	if runes := []rune(content); len(runes) > 500 {
		sample = string(runes[:500])
	}

	namespace := grailStr(record, "k8s.namespace.name")
	workload := grailStr(record, "k8s.workload.name")
	container := grailStr(record, "k8s.container.name")

	// Build Prometheus-compatible container_id for ticket reference linking.
	containerID := ""
	if namespace != "" && workload != "" {
		containerID = fmt.Sprintf("/k8s/%s/%s", namespace, workload)
	}

	patternHash := generatePatternHash(content)

	// Extract severity: prefer "status", fall back to "loglevel", default to "error".
	level := grailStr(record, "status")
	if level == "" {
		level = grailStr(record, "loglevel")
	}
	if level == "" {
		level = "error"
	}

	return LogGroup{
		Sample:      sample,
		Namespace:   namespace,
		Workload:    workload,
		Container:   container,
		ContainerID: containerID,
		PatternHash: patternHash,
		Level:       level,
		Count:       int64(math.Round(count)),
		Timestamps:  []int64{endTimeSec},
		Values:      []float64{count},
	}, true
}
